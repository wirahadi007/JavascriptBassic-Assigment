let hari= 3;

switch (hari) {
    case 1:
        console.log("minggu")
        break;
    case 2:
        console.log("senin")
        break;
    case 3:
        console.log("selasa")
        break;
    case 4:
        console.log("rabu")
        break;
    case 5:
        console.log("kamis")
        break;
    case 6:
        console.log("jumat")
        break;
    case 7:
        console.log("sabtu")
        break;
    default:
        console.log("hari yang anda cari tidak tersedia")
        break;
}