let simpleGame= "BOTTOM"

switch (simpleGame) {
    case "UP":
        console.log("Karakter berjalan ke atas")
        break;
    case "RIGHT":
        console.log("Karakter berjalan ke kanan")
        break;
    case "BOTTOM":
        console.log("Karakter berjalan ke bawah")
        break;
    case "LEFT":
        console.log("Karakter berlaja ke kiri")
        break;
    default:
        console.log("Perintah tidak ditemukan")
}